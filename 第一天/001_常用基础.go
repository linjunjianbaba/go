// 标准命令概述
// build：用于编译给定的代码包或go命令后遗留的目录和文件。
// clean：用于清楚执行其他go命令后遗留的目录和文件
// doc：用于执行godoc命令已打印指定代码包
// env：用于打印go语言环境信息
// fix：用于执行go tool fix命令已修正给定代码包文件中包含的过时语法和代码调用
// fmt：用于执行gofmt命令以格式化给定代码包中源码文件
// get：用于下载和安装给定代码包及依赖包（提前安装git或hg）
// list：用于显示给定代码包的信息
// run：用于编译并运行给定的命令源码文件
// install：编译包文件并编译整个晨旭
// test：用于测试给定代码包
// tool：用于运行Go语言的特殊工具
// version：用于显示当前安装的Go语言的版本信息